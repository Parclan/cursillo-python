# Como utilizar int, float y str "string"


# int tambien convierte valores de coma ﬂotante a entero
numero = int(input("Digite un numero entero: "))

# funcion float que convierte enteros y cadenas en numeros en coma ﬂotante
numero1 = float(input("Digite un numero: "))

numero2 = str(input("Digite un cualquier cosa (Asi sea letra o numero): "))


# Numero Entero
print(numero)

# Numero Decimal
print(numero1)

#Variable string
print(numero2)